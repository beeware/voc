def _string_substitutions(string):
    delims = (string[0], string[-1])
    string = string[1:-1]
    elems = [elem for elem in string.split(", ")]
    perms = permutations(elems)
    return [(delims[0] + ", ".join(list(perm)) + delims[1]) for perm in perms]


